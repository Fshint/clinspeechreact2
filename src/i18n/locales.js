export const TRANSLATIONS = {
  kk: KK,
};

export function getStoredLocale() {
  if (typeof window === 'undefined') return LOCALES.ru;
  return localStorage.getItem('app_locale') || LOCALES.ru;
}

export function setStoredLocale(locale) {
  if (typeof window === 'undefined') return;
  localStorage.setItem('app_locale', locale);
}

export function translate(locale, text, vars = {}) {
  if (!text) return text;
  if (locale === LOCALES.ru) {
    return interpolate(text, vars);
  }
  const translated = TRANSLATIONS[locale]?.[text] || text;
  return interpolate(translated, vars);
}

export function interpolate(text, vars = {}) {
  return String(text).replace(/\{(\w+)\}/g, (_, key) => (
    Object.prototype.hasOwnProperty.call(vars, key) ? String(vars[key]) : `{${key}}`
  ));
}

